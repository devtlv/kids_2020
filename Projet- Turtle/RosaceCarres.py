import turtle
def draw_square(steps, inclinaison):
    t = turtle.Pen()
    t.right(inclinaison)
    t.forward(steps)
    t.right(90)
    t.forward(steps)
    t.right(90)
    t.forward(steps)
    t.right(90)
    t.forward(steps)
    t.right(90)
    t.forward(steps)

draw_square(100, 100)

